// Object type variable
// use type for object
var user1;
// assign value to object
user1 = {
    name: 'John Doe',
    age: 25
};
// use type for object with optional variable
var user2;
// assign value to object including optional variable
user2 = {
    name: 'John Doe',
    age: 25,
    gender: "male"
};
